void setOnePixel(Pixel pixel, uint8_t* dataFrame16) {
	uint8_t y_sec = pixel.y / 4;
	uint8_t x_sec = pixel.x / 8;
	uint8_t y_sub = pixel.y % 4;
	uint8_t x_sub = pixel.x % 8;
	dataFrame16[x_sec * 4 + y_sec] &= ~(1 << (7 - x_sub));	
}

void clearScreen(uint8_t* dataFrame16) {
	for (uint8_t i = 0; i < 16; i++) {
		dataFrame16[i] = 0xff;
	}
}
void printOnePixel(Pixel pixel, uint8_t dataFrame[16]) {
	clearScreen(dataFrame);
	setOnePixel(pixel, dataFrame);	
	setABMux(pixel.y % 4);
	for (uint8_t i = 0; i < 16; i++) {
		HAL_GPIO_WritePin(SCLK_GPIO_Port, SCLK_Pin, GPIO_PIN_SET);
		HAL_SPI_Transmit(&hspi1, &dataFrame[i], 1, 10);
		HAL_GPIO_WritePin(SCLK_GPIO_Port, SCLK_Pin, GPIO_PIN_RESET);
	}
}

void setMultiplePixel(uint8_t matrix[16][32], uint8_t dataFrame[4][16]) {
	for (uint8_t i = 0; i < 4; i++) {
		clearScreen(dataFrame[i]);
	}
	Pixel pix;
	for (uint8_t x = 0; x < 32; x++) {
		for (uint8_t y = 0; y < 16; y++) {
			pix.x = x;
			pix.y = y;
			if (matrix[y][x] == 1) {
					setOnePixel(pix, dataFrame[y % 4]);
			}
		}		
	}
}

void printMultiplePixel(uint8_t dataFrame[4][16]) {
	for (uint8_t j = 0; j < 4; j++) {
		setABMux(j);
		for (uint8_t i = 0; i < 16; i++) {
			HAL_GPIO_WritePin(SCLK_GPIO_Port, SCLK_Pin, GPIO_PIN_SET);
			HAL_SPI_Transmit(&hspi1, &dataFrame[j][i], 1, 2);
			HAL_GPIO_WritePin(SCLK_GPIO_Port, SCLK_Pin, GPIO_PIN_RESET);
		}
		HAL_Delay(1);
	}
}

